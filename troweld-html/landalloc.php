              <p>
                 <strong> Background Information</strong> <br>
                By section I of the LUA all land in the territory of each State became vested in the Governor of the State who holds the land in trust and administers it for the use and common benefit of all Nigerians in accordance with the LUA. Section 5 of the LUA empowers the Governor of the State, in respect of land situate either in the urban or rural area, to grant Statutory Right of Occupancy to any person for all purposes. Section 6 of LUA also empowers the Local Government, in respect of land not being in the urban area, to grant Customary Right of Occupancy to any person or organisation for the use of the land in the Local Government Area for agricultural, residential and other purposes. Sections 34 and 36 of the LUA, respectively, deem an occupier or holder of land in urban and non-urban arrears, respectively, before the enactment of the LUA, to be a holder of Statutory or Customary Right of Occupancy. A Right of Occupancy is therefore the highest interest any person or organisation may have in land in Nigeria <br>
              </p>

              <p>
                   Section 9 of the LUA empowers the Governor, to the exclusion of any other person, body or authority, to issue Certificate of Occupancy to any person or organisation as evidence of his Statutory or Customary Right of Occupancy over any land. Therefore any person or organisation wishing to be issued a Certificate of Occupancy may apply to the Governor (through his delegate, the Ministry of Lands and Housing) in the manner following:
              </p>

