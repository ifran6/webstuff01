            <div class="p-5">
               <h6> <strong>Summary of Laws governing the administration of land in Ebonyi State</strong> </h6>
               <p>
               The following laws govern the administration of land in Ebonyi State:
      <ol>  
        <li> 
            <strong>
                Land Use Act, CAP 203, Laws of the Federation of Nigeria, 1990
            </strong>
            <p>
                The Land Use Act ("the LUA") is the most important legislation affecting the administration of land in Nigeria. Though a federal legislation, it applies in all states of the federation. .
                The LUA vests all land in the territory of each State ofthe federation (except land vested in Federal Government or its agency) in the Governor of the State, who shall hold the land in trust 
                and administer same for the use and common benefit of all Nigerians in accordance with the provisions of the Act. 
                As provided by the LUA all land in the urban areas in a state shall be under the control and management of the Governor and all other 
                land shall be under the control and management of the Local Government Area with jurisdiction over the area in which the land is situated. 
                As part of its power of administration of land within a state, the Governor is empowered by the Act, inter alia, 
                to grant Statutory Right of occupancy in respect of which the grantee shall be issued a Certificate of Occupancy ) 
                to any person for all purpose. A similar power is granted to the Local Government to grant Customary Right of Occupancy to any person or organization for use of land in the Local Government Area for agricultural, residential and other purposes. Any Right of Occupancy granted under the Act shall be for a definite term. The Governor may also revoke any Right of Occupancy upon certain conditions as stipulated in the LUA.
                The LUA also provides that the consent of the Governor is required for any valid transfer or alienation of any interest in land.
            </p>
            <p>
                The LUA permits the Governor to delegate all or any of his powers under the Act to the State Commissioner of Land.
            </p>
        </li>
        <li>
        <strong> Land Use Law, CAP 120, Revised Laws of Ebonyi State, 2004:</strong> <br>
        The Land Use Law ("the Law") is the State version of the LUA. Essentially it makes provision for the vesting, 
        occupation and use of land in Ebonyi State . 
        It is intended to provide a more detailed framework for the administration of the powers vested in the Governor under the LUA. 
        The Law provides that the Governor shall issue a Certificate of Occupancy for a term not exceedin 99 years. 
        The aw also enables the Governor to revise and fix yearly rent to be paid by the holder of the Certificate of Occupancy. 
        Under the Law, no Statutory Right of Occupancy shall be granted with respect to any land in the State unless such land has been surveyed and demarcated by a Government or Licensed/Registered Surveyor
        </li>
        <li>
            <b> State Lands Law, CAP 144, Revised Laws of Ebonyi State, 2004:</b><br>
            The State Lands Law ("the SLL") was enacted to make further and better provisions for the management and disposal of State Lands. 
            Under the SLL, except as otherwise provided, there shall be implied in every Certificate of Occupancy issued by virtue of the Law/LUA certain covenants. 
            These includes covenants to pay rent, taxes, rates, charges, duties, assessment or outgoings of whatever description as may be imposed, 
            charged, charged or assessed upon the lands comprised in such certificate of occupancy. 
            The SLL also provides that any person authorized by the Governor may at any time enter upon any land sold or granted under the Law/LUA 
            and resume any part thereof for the purpose carrying out any act or duty beneficial to the public. 
            It also repeats the provision of the Law to the effect that shall be surveyed before sale 
            and any buyer thereof shall be obliged to maintain boundary marks.

        </li>
        <li>
            <b>Land Instruments (preparation and Registration Law), CAP 100, Revised,Laws of Ebonyi State, 2004:</b> <br>
            The Land Instruments (Preparation and Registration) Law ("the LIPRL") was enacted to prohibit unqualified persons from drafting and preparing instruments/documents affecting lands in the state;
            and to regulate the registration of instruments and the filing of judgments affecting land in the State.
            The LIPRL provides that only legal practitioners shall either directly or indirectly, for or in the expectation of a fee, 
            gain or reward, prepare instruments affecting land in the state. It is an offence for any person not being a legal practitioner,
            except a public officer, preparing such in the course of his duty, to prepare any instrument affecting land in expectation of any fee, 
            gain or reward.
                <br> <br>
            The LIRPL also establishes the Land Registry, Enugu as the proper place for registration of all instrument including power of attorney, affecting land in the state. The LIPRL also defines instruments that are registrable in the Lands Registry. Any registrable instrument that is not registered shall not be pleaded or given in evidence in any court as affecting land. The LIPRL also requires every judgment affecting land in the state to be registered within 2 months of the delivery of the judgment.
        </li>
        <li>
            <b> Town Planning Law, CAP 151, Revised Laws of Ebonyi State 2004:</b> <br>
            The Town Planning Law ("the TPL") makes provision for the re-planning, improvement and development of different parts of the state. Under the TPL, upon the declaration of a planning area, the Town Planning Authority shall either frame a planning scheme for the area or any part thereof or adopt without modification any scheme proposed by any or all of the owners of the land within such area. The TPL also empowers the Town Planning Authority to execute and enforce any approved scheme. The Town Planning Authority is also empowered, for the purpose of enforcing and carrying into effect of any scheme may at any time remove, pull down, or alter, so as to bring into conformity with the provisions of the scheme, any building or other work which is inconsistent with the scheme
        </li>
        <li>
            <b>Public Land Acquisition Law, CAP 129, Revised Laws of Ebonyi State, 2004:</b> <br>
            The Public Land Acquisition Law ("the PLAL") grants power to the Government to acquire land for public purposes. Where it appears to the Commissioner for Lands that land in any locality is needed for public purposes, the Commissioner is empowered to authorize any person to enter upon the land to survey and take levels of any land in the locality, dig or bore under the subsoil, to do any other acts necessary to ascertain whether the land is adapted for such purpose. The PLAL provides for the payment of compensation to persons entitled to land, which has been acquired thereunder.  
        </li>
        <li>
            <b>Acquisition of Land by Aliens Law, CAP 3, Revised Laws of Ebonyi State, 2004:</b> <br>
            The Acquisition of Land by Aliens Law ("the ALAL") makes provision for regulating the procedure for the acquisition by aliens of land in the state and for other matter connected therewith. The ALAL defines an "alien" to mean any individual other than a Nigerian, or any company or association or body of persons corporate or unincorporated other than a body corporate established under any written law which empowers that body to acquire property, etc.
        </li>
        <li>
            <b>Survey Law, CAP 147, Revised Laws of Ebonyi State:</b> <br>
            The Survey law ("the SL") provides for the amendment and consolidation of the law relating to survey of lands in Ebonyi State.
        </li>
        <li>
            <b>Land Development (Provisions for Roads) Law, CAP 99, Revised Laws of Ebonyi State, 2004:</b> <br>
            The Land Development, (Provisions for Roads) Law ("the LDPRL") makes provision for the reservation of land for roads where land is sold off in lots.
        </li>
        <li>
            <b>Building Plans (Special Provisions) Law, CAP18, Revised Laws of Ebonyi State 2004:</b> <br>
            Building Plans (Special Provisions) Law ("the BPSPL") provides for the speedy approval of building plans in Ebonyi State by the Town planning Authority. By virtue of the BPSL, any application for approval of building plan, which is not rejected or approved after 30 (thirty) days of the submission of the application, shall be deemed to be approved by the relevant Authority.
       </li>
        
    </ol>
            
            
